public class Calc
{
    public double Addi(double tall1, double tall2)
    {
        return tall1 + tall2;
    }
    public double Minu(double tall1, double tall2)
    {
        return tall1 - tall2;
    }
    public double Divi(double tall1, double tall2)
    {
        return tall1 / tall2;
    }
    public double Multi(double tall1, double tall2)
    {
        return tall1 * tall2;
    }
}